    private int gerarNovoIdLivro() {
        int max = 0;
        for(Livro l : listaLivros) {
            if(l.getId() > max) max = l.getId();
        }
        return max + 1;
    }
    
    private int gerarNovoIdCategoria() {
        int max = 0;
        for(Categoria c : listaCategorias) {
            if(c.getId() > max) max = c.getId();
        }
        return max + 1;
    }
    
    private void carregarDadosIniciais() {
        try {
            listaCategorias.clear();
            listaLivros.clear();
            listaMovimentacoes.clear();
            
            jComboBoxCategorias.removeAllItems();

            List<String> linhasCat = arquivo.ler("categorias.txt");
            for (String linha : linhasCat) {
                String[] partes = linha.split(";");
                if(partes.length >= 2) { 
                    listaCategorias.add(new Categoria(Integer.parseInt(partes[0]), partes[1]));
                    jComboBoxCategorias.addItem(partes[0] + " - " + partes[1]);
                }
            }
            
            List<String> linhasLiv = arquivo.ler("livros.txt");
            for (String linha : linhasLiv) {
                String[] partes = linha.split(";");
                if(partes.length >= 6) {
                    listaLivros.add(new Livro(Integer.parseInt(partes[0]), partes[1], partes[2], 
                            Integer.parseInt(partes[3]), Integer.parseInt(partes[4]), partes[5]));
                }
            }
            atualizarTabelaLivros();

            List<String> linhasMov = arquivo.ler("movimentacoes.txt");
            for (String linha : linhasMov) {
                String[] partes = linha.split(";");
                if(partes.length >= 6) {
                    listaMovimentacoes.add(new Movimentacao(Integer.parseInt(partes[0]), 
                            Integer.parseInt(partes[1]), partes[2], partes[3], partes[4], partes[5]));
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar dados! Verifique os arquivos.");
        }
    }

    private void salvarAlteracoesLivros() {
        List<String> linhas = new ArrayList<>();
        for (Livro l : listaLivros) {
            linhas.add(l.toLinhaArquivo());
        }
        arquivo.reescreverArquivo("livros.txt", linhas);
    }

    private void atualizarTabelaLivros() {
        DefaultTableModel model = (DefaultTableModel) jTableLivros.getModel();
        model.setRowCount(0); // Limpa as linhas
        for (Livro l : listaLivros) {
            model.addRow(new Object[]{l.getId(), l.getTitulo(), l.getStatus()}); // Exibe ID, Título e Status
        }
    }

