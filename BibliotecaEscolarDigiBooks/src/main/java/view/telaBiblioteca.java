    private void jButtonEditarLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarLivroActionPerformed
        // TODO add your handling code here:
        int row = jTableLivros.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um livro na tabela!");
            return;
        }

        int id = Integer.parseInt(jTableLivros.getValueAt(row, 0).toString()); //Pega ID da tabela e busca o  Livro
        Livro livroEdit = null;
        for (Livro l : listaLivros) {
            if (l.getId() == id) {
                livroEdit = l;
            }
        }

        if (livroEdit != null) {
            String t = JOptionPane.showInputDialog("Novo Título:", livroEdit.getTitulo());
            String a = JOptionPane.showInputDialog("Novo Autor:", livroEdit.getAutor());
            if (t != null && !t.isEmpty() && a != null && !a.isEmpty()) {
                livroEdit.setTitulo(t);
                livroEdit.setAutor(a);
                salvarAlteracoesLivros();
                atualizarTabelaLivros();
            }
        }

    }//GEN-LAST:event_jButtonEditarLivroActionPerformed

    private void jButtonCadastrarLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarLivroActionPerformed
           // TODO add your handling code here:
           try {
           if (jTFTituloLivro.getText().isEmpty() || jTFAutorLivro.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha Título e Autor.");
                return;
            }
            if (jComboBoxCategorias.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Cadastre uma categoria antes!");
                return;
            }
            String titulo = jTFTituloLivro.getText();
            String autor = jTFAutorLivro.getText();
            int ano;
            try {
                ano = Integer.parseInt(jTFAnoLivro.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Ano inválido! Digite apenas números.");
                return;
            }
            String catSel = (String) jComboBoxCategorias.getSelectedItem();
            int idCat = Integer.parseInt(catSel.split(" - ")[0]);

            int novoId = gerarNovoIdLivro();
            Livro novoLivro = new Livro(novoId, titulo, autor, idCat, ano, "Disponível");

            listaLivros.add(novoLivro);
            arquivo.escrever("livros.txt", novoLivro.toLinhaArquivo());
            atualizarTabelaLivros();
            JOptionPane.showMessageDialog(this, "Livro salvo!");

            jTFTituloLivro.setText("");
            jTFAutorLivro.setText("");
            jTFAnoLivro.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro inesperado. Verifique os dados.");
        }
    
    }//GEN-LAST:event_jButtonCadastrarLivroActionPerformed

    private void jButtonRegistrarMovimentacoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarMovimentacoesActionPerformed
        // TODO add your handling code here:
        try {
            int idLivro;
            try {
                idLivro = Integer.parseInt(jTFIDLivro.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "ID do Livro deve ser numérico!");
                return;
            }

            String responsavel = jTFNomeLocatario.getText();
            if (responsavel.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Digite o responsável!");
                return;
            }

            String tipo = (String) jComboBox1.getSelectedItem();
            String obs = jTFObservacoes.getText().isEmpty() ? "-" : jTFObservacoes.getText();

            Livro livroAlvo = null;
            for (Livro l : listaLivros) {
                if (l.getId() == idLivro) {
                    livroAlvo = l;
                }
            }

            if (livroAlvo == null) {
                JOptionPane.showMessageDialog(this, "Livro ID " + idLivro + " não encontrado!");
                return;
            }

            if (tipo.equals("Empréstimo")) {
                if (!livroAlvo.getStatus().equals("Disponível")) {
                    JOptionPane.showMessageDialog(this, "Livro indisponível!");
                    return;
                }
                livroAlvo.setStatus("Emprestado");
            } 
            else {
                if (tipo.equals("Devolução")) {
                livroAlvo.setStatus("Disponível");
                }
                else {
                    if (tipo.equals("Perda")) {
                    livroAlvo.setStatus("Perdido");
                    } 
                    else {
                        if (tipo.equals("Danificado")) {
                        livroAlvo.setStatus("Danificado");
                        }
                    }
                }
            }

            int idMov = listaMovimentacoes.size() + 1;// Cria e salva a movimentação do histórico
            String data = LocalDate.now().format(DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            Movimentacao mov = new Movimentacao(idMov, idLivro, tipo, responsavel, data, obs);

            listaMovimentacoes.add(mov);
            arquivo.escrever("movimentacoes.txt", mov.toLinhaArquivo());

            salvarAlteracoesLivros();
            atualizarTabelaLivros();
            JOptionPane.showMessageDialog(this, "Movimentação registrada!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro inesperado. Verifique os dados.");
        }
    }//GEN-LAST:event_jButtonRegistrarMovimentacoesActionPerformed

    private void jButtonEditarCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarCategoriaActionPerformed
        // TODO add your handling code here:
        String idTxt = JOptionPane.showInputDialog("Digite o ID da Categoria para editar:");
        if (idTxt != null) {
            try {
                int id = Integer.parseInt(idTxt);
                Categoria catEdit = null;

                for (Categoria c : listaCategorias) {
                    if (c.getId() == id) {
                        catEdit = c;
                    }
                }

                if (catEdit != null) {
                    String novaDesc = JOptionPane.showInputDialog("Nova Descrição:", catEdit.getDescricao());
                    if (novaDesc != null && !novaDesc.isEmpty()) {
                        catEdit.setDescricao(novaDesc);

                        List<String> linhas = new ArrayList<>();
                        for (Categoria c : listaCategorias) {
                            linhas.add(c.toString());
                        }
                        arquivo.reescreverArquivo("categorias.txt", linhas);

                        jComboBoxCategorias.removeAllItems();
                        for (Categoria c : listaCategorias) {
                            jComboBoxCategorias.addItem(c.getId() + " - " + c.getDescricao());
                        }

                        JOptionPane.showMessageDialog(this, "Categoria Editada com sucesso!");
                    }
                } 
                else {
                    JOptionPane.showMessageDialog(this, "ID não encontrado.");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "ID inválido! Digite um número inteiro.");
            }
        }
    }//GEN-LAST:event_jButtonEditarCategoriaActionPerformed

    private void jButtonGerarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGerarRelatorioActionPerformed
        // TODO add your handling code here:
        int tipo = jComboBoxRelatorios.getSelectedIndex();
        jTARelatorio.setText(""); // Limpa a área de texto
        StringBuilder sb = new StringBuilder();

        if (tipo == 0) { //Livros Disponíveis
            sb.append("=== LIVROS DISPONÍVEIS ===\n");
            for (Livro l : listaLivros) {
                if (l.getStatus().equals("Disponível")) {
                    sb.append(l.getTitulo()).append("\n");
                }
            }
        }
        else if (tipo == 1) {  //Histórico de um Livro Específico
            String idTxt = JOptionPane.showInputDialog("ID do Livro:");
            if (idTxt != null) {
                try {
                    int id = Integer.parseInt(idTxt);
                    sb.append("HISTÓRICO DO LIVRO ID ").append(id).append(":\n");
                    for (Movimentacao m : listaMovimentacoes) {
                        if (m.getIdLivro() == id) {
                            sb.append(" - ").append(m.getData()).append(": ").append(m.getTipo())
                                    .append(" (Resp: ").append(m.getResponsavel()).append(")\n");
                        }
                    }
                } catch (NumberFormatException e) {
                    sb.append("ID Inválido.");
                }
            }
        } 
        else if (tipo == 2) { //3. Empréstimos no Mês
            String mes = JOptionPane.showInputDialog("Mês/Ano (ex: 11/2024):");
            if (mes != null && !mes.isEmpty()) {
                int count = 0;
                for (Movimentacao m : listaMovimentacoes) {
                    if (m.getTipo().equals("Empréstimo") && m.getData().contains(mes)) {
                        count++;
                    }
                }
                sb.append("Total de Empréstimos em ").append(mes).append(": ").append(count);
            }
        } 
        else if (tipo == 3) { //Livros Mais Emprestados Por Ano
            String ano = JOptionPane.showInputDialog("Digite o Ano (ex: 2024):");
            if (ano != null && !ano.isEmpty()) {
                sb.append("=== LIVROS MAIS EMPRESTADOS EM ").append(ano).append(" ===\n");
                for (Livro l : listaLivros) {
                    int qtd = 0;
                    for (Movimentacao m : listaMovimentacoes) {
                        if (m.getIdLivro() == l.getId()
                                && m.getTipo().equals("Empréstimo")
                                && m.getData().endsWith(ano)) {
                            qtd++;
                        }
                    }
                    if (qtd > 0) {
                        sb.append(l.getTitulo()).append(": ").append(qtd).append(" vezes\n");
                    }
                }
            }
        } 
        else if (tipo == 4) { // Perdidos ou Danificados
            sb.append("=== PERDIDOS OU DANIFICADOS ===\n");
            for (Livro l : listaLivros) {
                if (l.getStatus().equals("Perdido") || l.getStatus().equals("Danificado")) {
                    sb.append(l.getTitulo()).append(" - Status: ").append(l.getStatus()).append("\n");
                }
            }
        } 
        else if (tipo == 5) { //Empréstimos Por Categoria
            sb.append("=== EMPRÉSTIMOS POR CATEGORIA ===\n");
            for (Categoria c : listaCategorias) {
                int count = 0;
                for (Movimentacao m : listaMovimentacoes) {
                    if (m.getTipo().equals("Empréstimo")) {
                        for (Livro l : listaLivros) {
                            if (l.getId() == m.getIdLivro() && l.getIdCategoria() == c.getId()) {
                                count++;
                            }
                        }
                    }
                }
                sb.append(c.getDescricao()).append(": ").append(count).append(" empréstimos\n");
            }
        }
        jTARelatorio.setText(sb.toString());

    }//GEN-LAST:event_jButtonGerarRelatorioActionPerformed

